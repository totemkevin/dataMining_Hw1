
public class NodeChr {
	private int[] chr;
	private int lengthOfChr;
	private int adapter;
	
	public void setChr(int[] chr)
	{
		this.chr=chr;
		lengthOfChr=chr.length;
	}
	public void setAdapter()
	{
		this.adapter=adapter;
	}
	
	
	public int[] getChr()
	{
		return chr;
	}
	public int getLength()
	{
		return lengthOfChr;
	}
	public int getAdapter()
	{
		return adapter;
	}
	
}
